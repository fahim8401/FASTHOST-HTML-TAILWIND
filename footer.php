 <script>
  document.querySelectorAll('.toggle-button').forEach(button => {
    button.addEventListener('click', () => {
      // Remove active styles from all buttons
      document.querySelectorAll('.toggle-button').forEach(btn => {
        btn.classList.remove('bg-green-500', 'text-white', 'border-green-500');
        btn.classList.add('bg-gray-300', 'text-gray-800', 'border-gray-300');
        btn.setAttribute('aria-pressed', 'false');
      });
      
      // Add active styles to the clicked button
      button.classList.remove('bg-gray-300', 'text-gray-800', 'border-gray-300');
      button.classList.add('bg-green-500', 'text-white', 'border-green-500');
      button.setAttribute('aria-pressed', 'true');
      
      // Hide all plan categories
      document.querySelectorAll('.plan-category').forEach(category => {
        category.classList.add('hidden');
      });
      
      // Show the selected plan category
      const target = button.getAttribute('data-target');
      document.getElementById(target).classList.remove('hidden');
    });
  });
</script>
